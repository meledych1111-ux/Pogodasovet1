  adviceText += `*–†–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏:*\n`;
  advice.forEach(item => {
    adviceText += `${item}\n`;
  });
  
  // –¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–Ω—ã–π —Å–æ–≤–µ—Ç
  adviceText += `\n*–¢–µ–º–ø–µ—Ä–∞—Ç—É—Ä–Ω—ã–µ —É—Å–ª–æ–≤–∏—è:*\n`;
  adviceText += `üå°Ô∏è –†–µ–∞–ª—å–Ω–∞—è: ${temp}¬∞C\n`;
