export function getWardrobeAdvice(weatherData) {
  const { temp, description, wind, precipitation } = weatherData;
  let advice = [];

  // 1. –ë–∞–∑–æ–≤—ã–π —Å–ª–æ–π –ø–æ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–µ
  if (temp >= 25) {
    advice.push('‚Ä¢ üëï –ë–∞–∑–æ–≤—ã–π —Å–ª–æ–π: –º–∞–π–∫–∞, —Ñ—É—Ç–±–æ–ª–∫–∞ –∏–∑ —Ö–ª–æ–ø–∫–∞ –∏–ª–∏ –ª—å–Ω–∞.');
    advice.push('‚Ä¢ üëñ –í–µ—Ä—Ö: —à–æ—Ä—Ç—ã, –ª–µ–≥–∫–∏–µ –±—Ä—é–∫–∏ –∏–ª–∏ —é–±–∫–∞.');
  } else if (temp >= 18) {
    advice.push('‚Ä¢ üëï –ë–∞–∑–æ–≤—ã–π —Å–ª–æ–π: —Ñ—É—Ç–±–æ–ª–∫–∞ –∏–ª–∏ —Ç–æ–Ω–∫–∞—è —Ä—É–±–∞—à–∫–∞.');
    advice.push('‚Ä¢ üß• –í–µ—Ä—Ö: –¥–∂–∏–Ω—Å—ã, –±—Ä—é–∫–∏, –º–æ–∂–Ω–æ –≤–∑—è—Ç—å –ª–µ–≥–∫—É—é –∫—É—Ä—Ç–∫—É –∏–ª–∏ –≤–µ—Ç—Ä–æ–≤–∫—É –Ω–∞ –≤–µ—á–µ—Ä.');
  } else if (temp >= 10) {
    advice.push('‚Ä¢ üëï –ë–∞–∑–æ–≤—ã–π —Å–ª–æ–π: –ª–æ–Ω–≥—Å–ª–∏–≤ –∏–ª–∏ —Ç–æ–Ω–∫–æ–µ —Ç–µ—Ä–º–æ–±–µ–ª—å–µ.');
    advice.push('‚Ä¢ üß• –í–µ—Ä—Ö: —Å–≤–∏—Ç–µ—Ä, —Ç–æ–ª—Å—Ç–æ–≤–∫–∞, –¥–∂–∏–Ω—Å—ã + –≤–µ—Ç—Ä–æ–≤–∫–∞ –∏–ª–∏ –¥–µ–º–∏—Å–µ–∑–æ–Ω–Ω–∞—è –∫—É—Ä—Ç–∫–∞.');
  } else if (temp >= 0) {
    advice.push('‚Ä¢ üëï –ë–∞–∑–æ–≤—ã–π —Å–ª–æ–π: —Ç–µ–ø–ª–æ–µ —Ç–µ—Ä–º–æ–±–µ–ª—å–µ –∏–ª–∏ —Ñ–ª–∏—Å.');
    advice.push('‚Ä¢ üß• –í–µ—Ä—Ö: —É—Ç–µ–ø–ª–µ–Ω–Ω—ã–π —Å–≤–∏—Ç–µ—Ä, –∑–∏–º–Ω—è—è –∫—É—Ä—Ç–∫–∞, —Ç–µ–ø–ª—ã–µ –±—Ä—é–∫–∏.');
  } else {
    advice.push('‚Ä¢ üëï –ë–∞–∑–æ–≤—ã–π —Å–ª–æ–π: –ø–ª–æ—Ç–Ω–æ–µ —Ç–µ—Ä–º–æ–±–µ–ª—å–µ, —Ñ–ª–∏—Å.');
    advice.push('‚Ä¢ üß• –í–µ—Ä—Ö: –ø—É—Ö–æ–≤–∏–∫ –∏–ª–∏ –∑–∏–º–Ω—è—è –∫—É—Ä—Ç–∫–∞ —Å –º–µ–º–±—Ä–∞–Ω–æ–π, —É—Ç–µ–ø–ª–µ–Ω–Ω—ã–µ —à—Ç–∞–Ω—ã.');
  }

  // 2. –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏–∏ –ø–æ –ø–æ–≥–æ–¥–Ω—ã–º —É—Å–ª–æ–≤–∏—è–º
  if (description.toLowerCase().includes('–¥–æ–∂–¥—å') || precipitation > 0) {
    advice.push('‚Ä¢ ‚òî –ó–∞—â–∏—Ç–∞ –æ—Ç –≤–ª–∞–≥–∏: –¥–æ–∂–¥–µ–≤–∏–∫, –∑–æ–Ω—Ç, –Ω–µ–ø—Ä–æ–º–æ–∫–∞–µ–º–∞—è –æ–±—É–≤—å –∏–ª–∏ –≤—ã—Å–æ–∫–∏–µ –±–æ—Ç–∏–Ω–∫–∏.');
  }
  if (description.toLowerCase().includes('—Å–Ω–µ–≥')) {
    advice.push('‚Ä¢ ‚ùÑÔ∏è –î–ª—è —Å–Ω–µ–≥–∞: –Ω–µ–ø—Ä–æ–º–æ–∫–∞–µ–º–∞—è –æ–±—É–≤—å —Å –≥–ª—É–±–æ–∫–∏–º –ø—Ä–æ—Ç–µ–∫—Ç–æ—Ä–æ–º, –≤–∞—Ä–µ–∂–∫–∏ —Ç–µ–ø–ª–µ–µ –ø–µ—Ä—á–∞—Ç–æ–∫.');
  }
  if (wind > 7) {
    advice.push('‚Ä¢ üí® –û—Ç –≤–µ—Ç—Ä–∞: –≤–µ—Ç—Ä–æ–≤–∫–∞ —Å –º–µ–º–±—Ä–∞–Ω–æ–π, –æ–¥–µ–∂–¥–∞ –±–µ–∑ –∫—Ä—É–ø–Ω—ã—Ö –æ—Ç–≤–µ—Ä—Å—Ç–∏–π, —à–∞—Ä—Ñ.');
  }
  if (description.toLowerCase().includes('—è—Å–Ω–æ') || description.includes('‚òÄÔ∏è')) {
    advice.push('‚Ä¢ üï∂Ô∏è –û—Ç —Å–æ–ª–Ω—Ü–∞: —Å–æ–ª–Ω—Ü–µ–∑–∞—â–∏—Ç–Ω—ã–µ –æ—á–∫–∏, –≥–æ–ª–æ–≤–Ω–æ–π —É–±–æ—Ä (–∫–µ–ø–∫–∞, –ø–∞–Ω–∞–º–∞).');
  }

  // 3. –ê–∫—Å–µ—Å—Å—É–∞—Ä—ã –∏ –æ–±—â–∏–µ —Å–æ–≤–µ—Ç—ã
  if (temp < 15) {
    advice.push('‚Ä¢ üß£ –ê–∫—Å–µ—Å—Å—É–∞—Ä—ã: —à–∞–ø–∫–∞, —à–∞—Ä—Ñ, –ø–µ—Ä—á–∞—Ç–∫–∏ (–µ—Å–ª–∏ —Ö–æ–ª–æ–¥–Ω–æ).');
  }
  if (temp > 20 && description.includes('—è—Å–Ω–æ')) {
    advice.push('‚Ä¢ üß¥ –ù–µ –∑–∞–±—É–¥—å—Ç–µ: —Å–æ–ª–Ω—Ü–µ–∑–∞—â–∏—Ç–Ω—ã–π –∫—Ä–µ–º SPF 30+.' );
  }

  advice.push('\nüëü *–û–±—É–≤—å*: –≤—ã–±–∏—Ä–∞–π—Ç–µ –ø–æ –ø–æ–≥–æ–¥–µ ‚Äî –∫—Ä–æ—Å—Å–æ–≤–∫–∏, –±–æ—Ç–∏–Ω–∫–∏, —Å–∞–Ω–¥–∞–ª–∏–∏.');
  advice.push('üéí *–° —Å–æ–±–æ–π*: –Ω–µ–±–æ–ª—å—à–∞—è —Å—É–º–∫–∞ –¥–ª—è —Å–Ω—è—Ç—ã—Ö —Å–ª–æ–µ–≤ –æ–¥–µ–∂–¥—ã, –µ—Å–ª–∏ —Å—Ç–∞–Ω–µ—Ç –∂–∞—Ä–∫–æ.');

  return advice.join('\n');
}
