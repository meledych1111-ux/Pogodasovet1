// Получить фразу дня (по дате)
export function getPhraseOfDay() {
  const today = new Date();
  const startDate = new Date('2024-01-01');
  const diffDays = Math.floor((today - startDate) / (1000 * 60 * 60 * 24));
  
  return dailyPhrases[diffDays % dailyPhrases.length];
}

// Получить фразу по категории
export function getPhraseByCategory(category) {
  const categoryPhrases = dailyPhrases.filter(phrase => phrase.category === category);
  if (categoryPhrases.length === 0) return getPhraseOfDay();
  
  const randomIndex = Math.floor(Math.random() * categoryPhrases.length);
  return categoryPhrases[randomIndex];
}

// Получить случайную фразу
export function getRandomPhrase() {
  const randomIndex = Math.floor(Math.random() * dailyPhrases.length);
  return dailyPhrases[randomIndex];
}

// Получить фразы по уровню сложности
export function getPhrasesByDifficulty(difficulty) {
  return dailyPhrases.filter(phrase => phrase.difficulty === difficulty);
}

// Получить все категории
export function getAllCategories() {
  const categories = new Set(dailyPhrases.map(phrase => phrase.category));
  return Array.from(categories);
}

// Получить статистику по фразам
export function getPhraseStats() {
  const stats = {
    total: dailyPhrases.length,
    byCategory: {},
    byDifficulty: {
      beginner: 0,
      intermediate: 0,
      advanced: 0
    }
  };
  
  dailyPhrases.forEach(phrase => {
    // Статистика по категориям
    if (!stats.byCategory[phrase.category]) {
      stats.byCategory[phrase.category] = 0;
    }
    stats.byCategory[phrase.category]++;
    
    // Статистика по сложности
    if (phrase.difficulty in stats.byDifficulty) {
      stats.byDifficulty[phrase.difficulty]++;
    }
  });
  
  return stats;
}

// Поиск фраз по ключевому слову
export function searchPhrases(keyword) {
  const searchTerm = keyword.toLowerCase();
  return dailyPhrases.filter(phrase => 
    phrase.english.toLowerCase().includes(searchTerm) ||
    phrase.russian.toLowerCase().includes(searchTerm) ||
    phrase.explanation.toLowerCase().includes(searchTerm)
  );
}
